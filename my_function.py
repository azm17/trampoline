    light_green = [164,202,57]
    light_pink=[255, 182, 193]
    tmp=0
    img_array = np.ones((len(array),len(array[0]), 3),np.uint8)*0
    for clist in [light_green, light_pink]:
        tmp+=1
        for i in range(len(array)):
            for j in range(len(array)):
                for k in range(3):
                    if array[i][j]==0:
                        img_array[i][j][k] = 0
                    elif array[i][j]==1:
                        img_array[i][j][k] = clist[k]
                    else:
                        img_array[i][j][k] = 255
        fig = plt.figure()
        plt.plot(), plt.imshow(img_array)
        plt.axis("off")
        fig.patch.set_alpha(0)
        plt.savefig('android{}.png'.format(tmp), dpi = dpi)

